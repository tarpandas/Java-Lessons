/**
 * Operators in Java
 * 
 */
public class OperatorsInJava {
    public static void main(String[] args) {
        // Read the document
        int a = 33;
        int b = 42;
        System.out.println(a<<2);
        System.out.println(b>>2);

        int c = -8;
        System.out.println(c>>2);
        System.out.println(c>>>2);
    }
}
